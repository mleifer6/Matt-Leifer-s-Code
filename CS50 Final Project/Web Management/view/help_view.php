<a name="all_food"> All Food</a>
<p class="paragraph">
    Once you have entered food items into your food log, you can view all your entries
    in the dropdown option "All Food" beneath "Food Log". There, you will be able to
    see all the food you have eaten. You can click on any item in your food log to see the nutritional
    facts in further detail.
</p>
<a name="food_history">Food History</a>
<p class="paragraph">
    Underneath "Food Log", you will also find "Food History". On this page you can enter
    two dates: a start date and an end date. The website will then find all the food you logged within that 
    time period and calculate several metrics so that you can better understand your eating habits. The 
    table on the left will display all the food you ate during that time window and the
    time that the food was logged and the one on the right will show you the total amount of nutrients 
    you consumed over this period as well as how much you consumed every day on average.  
    The values in the Percent of Recomended Daily are derived from <a href="http://www.fda.gov/Food/GuidanceRegulation/GuidanceDocumentsRegulatoryInformation/LabelingNutrition/ucm064928.htm">those</a>
    calculated by the FDA. 
</p>
<a name="search">Search</a>
<p class="paragraph">
    To find food items, you can head over to the "Search" page and 
    enter your desired item into the search bar. Once the results have appeared,
    you can click on any item to see the additional nutritional information such as the number of 
    calories or the amount of protein and the amount of fat the food contains.
    If you have had a particular food and would like to add it to your food log, you can enter the amount of
    you ate and then press the "+" button to add it to your food log. If you were unable to find the food you wanted,
    you can enter the nutritional information about your food and add it to your food log. You can find this option at 
    the bottom of the search page. 
</p>
<a name="recommendations">Recommendations</a>
<p class="paragraph">
    The Recommendations page gives you a recommendation based on all of the food items you have entered into your food log.
    You can learn more about any food item's nutritional information by clicking on it and a pop-up window will
    appear with all the relevant information. Recommendations are given by food group, the options being Dairy,
    Meat, Fruit, Vegetable, Nuts, Oils, Dessert, and Drinks.
</p>


